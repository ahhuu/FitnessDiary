package com.cz.fitnessdiary.utils;

public class ExerciseGuideHelper {

    /**
     * 根据动作名称和肌肉部位，生成详尽、专业的动作指南与要领
     */
    public static String getDetailedGuide(String name, String bodyPart, String defaultDesc) {
        if (name == null) return defaultDesc;
        String lowerName = name.toLowerCase();

        // 1. 卧推、推胸类
        if (lowerName.contains("卧推") || lowerName.contains("推胸")) {
            return "【起跑姿势】仰卧在平板凳上，双脚平踩在地面，脊柱挺直，收紧肩胛骨并使其保持下沉锁定，下背部微拱（能容纳一个手掌）。\n" +
                   "【发力轨迹】双手握距略宽于肩，控制杠铃/哑铃垂直下放至上胸或乳头下沿，大臂与躯干呈45°至70°夹角，手腕保持中立不后折。\n" +
                   "【呼吸发力】吸气时缓慢将负重放至胸前，稍作停顿；呼气时胸肌主动收缩发力，将负重垂直推起，顶点时手肘微屈不要锁死。\n" +
                   "【核心提示】全程保持肩胛骨收紧下沉，切忌推起时耸肩代偿，保护肩袖关节。";
        }

        // 2. 飞鸟夹胸类
        if (lowerName.contains("飞鸟") || lowerName.contains("夹胸")) {
            return "【起跑姿势】仰卧于斜凳上，或者站在龙门架中心。双手持握负重，掌心相对。双肘微屈（呈120°左右）并在运动中锁死该角度。\n" +
                   "【发力轨迹】将双臂向身体两侧画出一条宽阔的弧线缓慢张开，下降至大臂与地面平行，感受胸大肌外侧的极度拉伸感。\n" +
                   "【呼吸发力】吸气控制大臂缓慢张开；呼气时胸肌内侧发力，将双臂像“抱大树”一样沿原弧线轨迹挤压回原位，动作顶点胸肌刻意收紧1-2秒。\n" +
                   "【核心提示】整个过程要锁死手肘角度，不要将动作做成“推举”。动作还原时切忌下落过深防止肩部拉伤。";
        }

        // 3. 俯卧撑类
        if (lowerName.contains("俯卧撑")) {
            return "【起跑姿势】双手撑地，距离略宽于肩（标准）或靠近（窄距）。核心、腹肌和臀部紧绷，身体从头到脚呈一条笔直的斜线。\n" +
                   "【发力轨迹】屈肘将身体作为一个整体平稳下沉，手肘向斜后方伸展，直至胸部距离地面大约2-3厘米处，手掌位于乳头线两侧。\n" +
                   "【呼吸发力】下落时吸气，控制重心；起推时呼气，胸大肌和肱三头肌蹬地发力，将身体推回原位，身体保持中立不塌腰。\n" +
                   "【核心提示】手肘严禁外展过大呈90°，这会对肩关节产生极大剪切力。塌腰或翘臀均属不标准，需用核心力量紧绷脊柱。";
        }

        // 4. 引体向上类
        if (lowerName.contains("引体向上") || lowerName.contains("澳洲引体") || lowerName.contains("引体")) {
            return "【起跑姿势】双手正握或反握单杠，握距略宽于肩，身体自然悬垂。背部肌肉保持张力，不能完全“死挂”，双脚离地。\n" +
                   "【发力轨迹】肩胛骨首先启动收紧并下沉，背阔肌主导收缩，将身体整体垂直向上拉起，尽量让下巴或者锁骨超过单杠。\n" +
                   "【呼吸发力】悬垂时准备；呼气背肌发力将身体拉起；吸气时有控制地缓慢下落，使身体还原至初始状态，保持肌肉紧绷。\n" +
                   "【核心提示】专注用手肘去“向下寻找肋骨”的发力感，而不是单靠手臂二头肌往上拉，下落到最低点时手肘微屈，严防拉伤。";
        }

        // 5. 划船类
        if (lowerName.contains("划船") || lowerName.contains("提拉")) {
            return "【起跑姿势】双脚与肩同宽，膝盖微屈。以髋关节为轴向后折叠（俯身45°-60°左右），脊柱绝对挺直中立，双手抓握负重悬垂。\n" +
                   "【发力轨迹】将负重沿着大腿前侧拉向小腹（靠近肚脐）方向，同时双肘贴紧身体向后上方收缩，顶峰时背部肩胛充分向中缝挤压。\n" +
                   "【呼吸发力】吸气在肌肉控制下缓慢还原负重；呼气背部发力，快速将负重拉起并收缩背肌，顶点稍作停顿。\n" +
                   "【核心提示】挺胸、收腹、塌腰是安全的基石，任何时候都不能出现弓背（猫背）状态，否则下背部竖脊肌极易受伤。";
        }

        // 6. 硬拉类
        if (lowerName.contains("硬拉")) {
            return "【起跑姿势】双脚踩实地面（与肩同宽），杠铃贴紧小腿前侧。屈髋屈膝，下蹲俯身，双手正握或正反握，脊柱完全挺直，肩胛位于杠铃正上方。\n" +
                   "•【发力轨迹】双脚蹬地发力伸膝，当杠铃通过膝盖时，髋部强力前送，将杠铃沿着身体外侧拉起直至身体完全站立直立。\n" +
                   "【呼吸发力】起拉前吸气，腹腔憋气紧绷核心，立直站定后呼气；下落时先推髋向后，杠铃过膝后屈膝缓慢还原，吸气。\n" +
                   "【核心提示】硬拉是全身后链动作。拉起过程中背部决不能有任何弧度，杠铃运动轨迹必须垂直向上并贴紧身体，切忌用腰部往后生拉硬拽。";
        }

        // 7. 深蹲类
        if (lowerName.contains("深蹲") || lowerName.contains("静蹲") || lowerName.contains("分腿蹲")) {
            return "【起跑姿势】双脚开立与肩同宽（或略宽），脚尖自然向外微张15°。双手抱头、持哑铃或将杠铃压在斜方肌上，挺胸收腹，脊柱中立。\n" +
                   "【发力轨迹】吸气向后下方坐下（屈髋屈膝），像坐椅子一样臀部后移。下蹲至大腿与地面平行或略低，膝盖方向必须与脚尖方向完全吻合。\n" +
                   "【呼吸发力】下蹲时吸气控制核心；呼气时，脚跟发力蹬地，臀大肌与股四头肌协同收缩，快速推起躯干站直。\n" +
                   "【核心提示】下蹲时重心置于脚底中部及脚后跟，不能让脚跟离地导致重心前移。膝盖绝对禁止内扣（X腿），防止半月板与韧带损伤。";
        }

        // 8. 弯举类
        if (lowerName.contains("弯举")) {
            return "【起跑姿势】双脚同宽站立，脊柱挺直，双肩下沉放松。双手持负重，大臂夹紧在身体两侧，手肘位置在动作全程锁死不动。\n" +
                   "【发力轨迹】肱二头肌收缩发力，将小臂朝上弯起至靠近肩膀，手腕保持笔直不要内折借力，在顶点位置对肱二头肌强力挤压1秒。\n" +
                   "【呼吸发力】弯起时呼气，动作速度要快；下落还原时吸气，控制速度在2秒左右，在重力阻抗下平稳下落。\n" +
                   "【核心提示】全程禁止晃动大臂或借用身体前后摆动来抛负重，必须孤立二头肌。下落到最低点时手臂微弯，不要完全松劲。";
        }

        // 9. 臂屈伸、下压类
        if (lowerName.contains("臂屈伸") || lowerName.contains("下压")) {
            return "【起跑姿势】持握负重，大臂夹在躯干两侧。手肘保持锁定（手肘作为唯一转动轴心）。脊柱微前倾，核心收紧。\n" +
                   "【发力轨迹】肱三头肌主导收缩，将手臂向下或向后完全伸直，在最低点/最远端将手臂伸直到180°并强力收紧三头肌。\n" +
                   "【呼吸发力】发力推直手臂时呼气；吸气时大臂保持不动，手肘慢慢弯曲将负重收回到起始高度，保持三头肌紧绷。\n" +
                   "【核心提示】避免耸肩和使用体重向下压。手肘绝不能在运动中前后晃动或向两侧张开，大臂紧贴身体才是孤立三头肌的法宝。";
        }

        // 10. 推举、平举、肩部动作
        if (lowerName.contains("推举") || lowerName.contains("平举") || lowerName.contains("阿诺德")) {
            return "【起跑姿势】坐姿或直立，核心紧绷保护腰椎。持负重于双耳旁（推举）或垂于体侧（平举），双肩放松，禁止耸肩。\n" +
                   "【发力轨迹】\n" +
                   "• 推举：三角肌发力将负重垂直推至头顶上方，手臂接近伸直但肘部不锁死。\n" +
                   "• 平举：大臂微屈锁死，三角肌发力将双臂向侧方/前方画弧线抬起至与肩同高。\n" +
                   "【呼吸发力】用力向上或向外抬起时呼气；控制下落还原时吸气，平稳匀速。\n" +
                   "【核心提示】平举时动作顶点大臂与肩同高即可，不需要抬得过高。全程绝对禁止耸肩，否则斜方肌将抢占主发力，导致溜肩。";
        }

        // 11. 卷腹、仰卧起坐、核心类
        if (lowerName.contains("卷腹") || lowerName.contains("抬腿") || lowerName.contains("仰卧起坐") || lowerName.contains("核心")) {
            return "【起跑姿势】仰卧在垫子上，膝盖弯曲双脚踩地，下背部（腰部）死死贴紧地面不能有空隙。双手悬空置于大腿或轻护耳后。\n" +
                   "【发力轨迹】腹直肌强烈收紧，像卷春卷一样将肩膀、上背部依次卷起离开地面，卷起时感觉肋骨被朝盆骨方向拉近，下背部绝不离地。\n" +
                   "【呼吸发力】身体向上卷起时呼气，将肺部空气吐尽以利于腹肌最大收缩；控制下落还原时吸气。\n" +
                   "【核心提示】不要用双手去硬拉颈椎向上，手只是起到保护作用，发力点完全在腹部。下落还原时肩膀不要完全着地，保持腹肌张力。";
        }

        // 12. 绳索面拉
        if (lowerName.contains("面拉")) {
            return "【起跑姿势】面朝龙门架站立，绳索高度调至眼部高度。双手持绳索两端（大拇指朝后）。双脚前后站立，身体微后倾。\n" +
                   "【发力轨迹】三角肌后束及上背部发力，将绳索朝脸部（额头或鼻子）方向拉起，同时外旋肩膀，使双手在拉至脸部两侧时，手肘高度略高于手腕，形成W形。\n" +
                   "【呼吸发力】拉向脸部时呼气并强力收敛后束；吸气时控制阻力平稳还原手臂。\n" +
                   "【核心提示】面拉的核心要领在于“手肘向外拉”加“肩关节外旋”，切忌拉成普通的划船动作，专注肩部后束的挤压。";
        }

        // 13. 高位下拉
        if (lowerName.contains("下拉")) {
            return "【起跑姿势】坐姿在器械凳上，大腿被挡板顶紧固定，双手正握横杆，握距宽于肩，上身微向后倾斜15°，挺胸抬头。\n" +
                   "【发力轨迹】肩胛骨首先下沉锁定，背阔肌收缩将横杆垂直向下拉至锁骨处，双肘顺着肋骨外侧收缩，大臂夹紧胸腔侧面。\n" +
                   "【呼吸发力】吸气控制横杆缓慢升起拉伸背肌；呼气背肌强力收缩，拉下横杆并收紧，顶点停顿1秒。\n" +
                   "【核心提示】严禁身体剧烈前后摇晃借力，手掌只起挂钩作用，用双肘下拉的理念带动负重。";
        }

        // 2. 兜底逻辑：根据部位进行智能分析
        if (bodyPart != null) {
            String part = bodyPart.toLowerCase();
            if (part.contains("胸")) {
                return "【起跑姿势】肩胛骨内收下沉并锁定，挺胸，腰部微拱抬高胸腔。\n" +
                       "【发力轨迹】保持大臂与躯干夹角约45°-70°，控制负重垂直推起或沿弧线抱夹合拢。\n" +
                       "【呼吸发力】吸气控制肌肉持续拉伸缓慢下落，呼气时胸大肌强烈收紧推起。\n" +
                       "【核心提示】全程严防借力耸肩，推起时不要锁死关节，防止肘部受压。";
            }
            if (part.contains("背")) {
                return "【起跑姿势】脊柱绝对挺直，核心绷紧，肩胛骨保持可以自如前伸和后缩的张力。\n" +
                       "【发力轨迹】背阔肌或上背部发力，将手肘向身体后下方拉近，夹紧躯干充分挤压背肌。\n" +
                       "【呼吸发力】呼气拉起时收背；吸气还原时控制负重，使背肌得到彻底拉伸。\n" +
                       "【核心提示】严禁弓背以防伤及下腰，想象双臂是挂钩，发力核心在手肘。";
            }
            if (part.contains("肩")) {
                return "【起跑姿势】躯干直立挺胸，腹部收紧锁死腰椎，双肩放松不刻意耸肩。\n" +
                       "【发力轨迹】三角肌发力，控制手臂向上垂直推举，或以手肘带动手臂画弧线平举。\n" +
                       "【呼吸发力】呼气肩部主动发力抬起；吸气平稳控制轨迹，缓慢恢复至原位。\n" +
                       "【核心提示】动作全程禁止出现耸肩，大臂抬起至与肩同高即可，防止斜方肌代偿。";
            }
            if (part.contains("臂")) {
                return "【起跑姿势】双肘贴紧躯干两侧并保持锁定，防止大臂在动作中产生前后晃动借力。\n" +
                       "【发力轨迹】肱二头肌收缩弯起小臂，或肱三头肌发力下压伸直手臂。\n" +
                       "【呼吸发力】主动肌收缩弯举/推直时呼气，顶点刻意挤压；吸气在肌肉张力下缓慢还原。\n" +
                       "【核心提示】切忌借用身体摆动或甩动腰部借力，务必做到手臂肌肉的孤立训练。";
            }
            if (part.contains("腿") || part.contains("臀")) {
                return "【起跑姿势】双脚踩实地面，脊柱和盆骨保持中立，视线朝向前方偏下。\n" +
                       "【发力轨迹】屈髋向后坐，膝盖朝脚尖方向下蹲，下蹲至大腿与地面平行后，脚跟发力蹬地立直。\n" +
                       "【呼吸发力】吸气控制节奏平稳下蹲，呼气时臀腿同时收缩，伸髋直立。\n" +
                       "【核心提示】重心踩实在脚掌中部或后跟，严禁膝盖内扣（O/X形腿），保护膝关节。";
            }
            if (part.contains("腹") || part.contains("核心")) {
                return "【起跑姿势】仰卧在垫子上，腰部平贴地面。腹肌收缩，双手不抱头防止扭伤颈椎。\n" +
                       "【发力轨迹】腹肌做卷缩运动，肋骨向盆骨方向缩短，肩膀依次抬离地面。\n" +
                       "【呼吸发力】起身腹肌挤压时呼气，将空气呼尽；还原下落时吸气，背部不完全放松。\n" +
                       "【核心提示】动作要慢，靠腹部的力量慢慢卷起，切忌用大腿或脖子的力量硬拉身体。";
            }
        }

        // 3. 最终兜底
        if (defaultDesc != null && !defaultDesc.trim().isEmpty()) {
            return defaultDesc;
        }
        return "【安全指南】保持核心紧绷与脊柱挺直，用正确的主发力肌肉去带动负重。动作起拉与下落时避免过度扭转关节，吸气控制、呼气发力。新手建议先从自重或轻重量开始，安全第一。";
    }
}
